import com.qmxtech.qmxmcstdlib.tile.computers.controlled.TEControlledColoredLight;

import java.util.Map;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'TESmartColoredLight' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class TESmartColoredLight extends TEControlledColoredLight
{
    // Public Methods

        @Override
        public Map< String, String > getDeviceInfo()
        {
            Map< String, String > deviceInfo = super.getDeviceInfo();

            deviceInfo.put( DeviceAttribute.Class, DeviceClass.Generic );
            deviceInfo.put( DeviceAttribute.Description, "A colored, computer controlled light fixture." );
            deviceInfo.put( DeviceAttribute.Product, "Smart Colored Light" );

            return deviceInfo;
        }
}

