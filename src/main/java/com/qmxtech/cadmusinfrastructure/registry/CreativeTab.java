import com.qmxtech.cadmusinfrastructure.BuildInfo;

import net.minecraft.creativetab.CreativeTabs;
import net.minecraft.item.ItemStack;
import net.minecraftforge.fml.relauncher.Side;
import net.minecraftforge.fml.relauncher.SideOnly;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// The 'CreativeTab' Class
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public final class CreativeTab extends CreativeTabs
{
    // Public Constants (Singleton Instance)

        public static final CreativeTabs INSTANCE = new CreativeTab();

    // Public Methods

        @SideOnly( Side.CLIENT ) @Override public ItemStack createIcon()
        {
            return new ItemStack( ContentRegistry.itemTabIcon );
        }

    // Private Constructor

        private CreativeTab()
        {
            super( BuildInfo.MOD_ID + ".creativeTab" );
        }
}

